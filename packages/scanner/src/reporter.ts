export function toSARIF(result: ScanResult): string {
  const sarif = {
    version: "2.1.0",
    $schema: "https://schemastore.azurewebsites.net/schemas/json/sarif-2.1.0.json",
    runs: [
      {
        tool: {
          driver: {
            name: "NexusForge Scanner",
            version: "0.1.0",
            informationUri: "https://github.com/0xgetz/nexusforge",
            rules: result.vulnerabilities.map((v) => ({
              id: v.id,
              shortDescription: { text: v.title },
              fullDescription: { text: v.description },
              helpUri: v.url,
              properties: {
                severity: v.severity,
                tags: v.cwe || [],
              },
            })),
          },
        },
        results: result.vulnerabilities.map((v) => ({
          ruleId: v.id,
          message: { text: `${v.package}@${v.version}: ${v.title}` },
          level: v.severity === "critical" || v.severity === "high" ? "error" : "warning",
          locations: [
            {
              physicalLocation: {
                artifactLocation: {
                  uri: result.ecosystem === "npm" ? "package.json" : "requirements.txt",
                },
              },
            },
          ],
        })),
      },
    ],
  };

  return JSON.stringify(sarif, null, 2);
}