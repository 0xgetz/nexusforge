import type { ScanResult, Severity } from "./types.js";

export function toJSON(result: ScanResult): string {
  return JSON.stringify(result, null, 2);
}

export function toMarkdown(result: ScanResult): string {
  const severityEmoji: Record<Severity, string> = {
    critical: "🔴",
    high: "🟠",
    medium: "🟡",
    low: "🔵",
    info: "⚪",
  };

  let md = `# NexusForge Security Report\n\n`;
  md += `**Project:** ${result.projectName}\n`;
  md += `**Scanned:** ${new Date(result.timestamp).toLocaleString()}\n`;
  md += `**Duration:** ${result.duration}ms\n`;
  md += `**Ecosystem:** ${result.ecosystem}\n\n`;

  md += `## Summary\n\n`;
  md += `| Metric | Value |\n|--------|-------|\n`;
  md += `| Security Score | **${result.summary.score}/100** |\n`;
  md += `| Total Dependencies | ${result.totalDependencies} |\n`;
  md += `| Vulnerable Packages | ${result.vulnerablePackages} |\n`;
  md += `| Total Vulnerabilities | ${result.summary.total} |\n\n`;

  md += `### Severity Breakdown\n\n`;
  md += `| Severity | Count |\n|----------|-------|\n`;
  md += `| ${severityEmoji.critical} Critical | ${result.summary.critical} |\n`;
  md += `| ${severityEmoji.high} High | ${result.summary.high} |\n`;
  md += `| ${severityEmoji.medium} Medium | ${result.summary.medium} |\n`;
  md += `| ${severityEmoji.low} Low | ${result.summary.low} |\n`;
  md += `| ${severityEmoji.info} Info | ${result.summary.info} |\n\n`;

  if (result.vulnerabilities.length > 0) {
    md += `## Vulnerabilities\n\n`;
    for (const vuln of result.vulnerabilities) {
      md += `### ${severityEmoji[vuln.severity]} ${vuln.id} — ${vuln.title}\n\n`;
      md += `- **Package:** \`${vuln.package}@${vuln.version}\`\n`;
      md += `- **Severity:** ${vuln.severity.toUpperCase()}`;
      if (vuln.cvss) md += ` (CVSS: ${vuln.cvss})`;
      md += `\n`;
      if (vuln.fixedIn) md += `- **Fix:** Upgrade to \`${vuln.fixedIn}\`\n`;
      if (vuln.cwe?.length) md += `- **CWE:** ${vuln.cwe.join(", ")}\n`;
      if (vuln.url) md += `- **Reference:** [${vuln.url}](${vuln.url})\n`;
      md += `\n${vuln.description}\n\n---\n\n`;
    }
  }

  const outdated = result.dependencies.filter((d) => d.outdated);
  if (outdated.length > 0) {
    md += `## Outdated Dependencies\n\n`;
    md += `| Package | Current | Latest |\n|---------|---------|--------|\n`;
    for (const dep of outdated) {
      md += `| ${dep.name} | ${dep.version} | ${dep.latest} |\n`;
    }
    md += `\n`;
  }

  md += `---\n*Generated by [NexusForge Scanner](https://github.com/0xgetz/nexusforge)*\n`;
  return md;
}

